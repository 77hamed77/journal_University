document.addEventListener("DOMContentLoaded", () => {
  const languageSwitcher = document.getElementById("language-switcher");
  const switcherButton = document.getElementById("language-switcher-button");
  const options = document.getElementById("language-options");
  const selectedFlag = document.getElementById("selected-flag");
  const selectedLangText = document.getElementById("selected-lang-text");

  // --- Get the saved language or default to Arabic ---
  let currentLanguage = localStorage.getItem("language") || "ar";

  // --- Function to change language and update the page ---
  const changeLanguage = (langCode) => {
    currentLanguage = langCode;
    localStorage.setItem("language", currentLanguage);
    updatePageLanguage();
    // Close the dropdown after selection
    languageSwitcher.classList.remove("active");
  };

  // --- Main function to update all text on the page ---
  const updatePageLanguage = () => {
    const lang = translations[currentLanguage];
    if (!lang) return;

    // 1. Update page direction and lang attribute
    // document.documentElement.dir = currentLanguage === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = currentLanguage;

    // 2. Update all elements with data-translate-key
    document.querySelectorAll("[data-translate-key]").forEach((element) => {
      const key = element.getAttribute("data-translate-key");
      const type = element.getAttribute("data-translate-type");
      if (lang[key]) {
        if (type === "placeholder") {
          element.placeholder = lang[key];
        } else if (key === "copyright") {
          element.innerHTML = lang[key].replace(
            "{{ current_year }}",
            new Date().getFullYear()
          );
        } else {
          // Use innerHTML to render HTML tags like <b>, <br>, <a>
          element.innerHTML = lang[key];
        }
      }
    });

    // 3. Update the language switcher's display
    const activeOption = options.querySelector(
      `li[data-lang="${currentLanguage}"]`
    );
    if (activeOption) {
      const flagSrc = activeOption.querySelector("img").src;
      const langText = activeOption.querySelector("span").textContent;
      selectedFlag.src = flagSrc;
      selectedLangText.textContent = langText;
    }
  };

  // --- Event Listeners ---

  // 1. Toggle dropdown when the button is clicked
  switcherButton.addEventListener("click", (event) => {
    event.stopPropagation(); // Prevent the window click event from firing immediately
    languageSwitcher.classList.toggle("active");
  });

  // 2. Change language when an option is clicked
  options.querySelectorAll("li").forEach((option) => {
    option.addEventListener("click", () => {
      const langCode = option.getAttribute("data-lang");
      changeLanguage(langCode);
    });
  });

  // 3. Close dropdown if clicking outside of it
  window.addEventListener("click", (event) => {
    if (!languageSwitcher.contains(event.target)) {
      languageSwitcher.classList.remove("active");
    }
  });

  // --- Initial page load ---
  updatePageLanguage();
});
